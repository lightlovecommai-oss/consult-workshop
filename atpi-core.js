function calcPotential(scores) {
  var unlocked = Math.round(Math.pow(scores.A/100 * scores.T/100 * scores.P/100 * scores.I/100, 0.8) * 1000);
  return { unlocked: unlocked, locked: 1000 - unlocked };
}

